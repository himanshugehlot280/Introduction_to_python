# Python end parameter
"""
The print() function in python always creates a new line.
but there is alsa a parameter for this function which can put other characters instead of new line
at the end which is called end parameter """

print('Welcome')
print('Welcome')
print('Welcome')
print('-------------------------------------------')
print('Hello',end='')
print('Hello')
print('Hello',end=' ')
print('Hello')
print('Hello',end=' and ')
print('Hello')
print('Hello',end='\n')
print('Hello')
